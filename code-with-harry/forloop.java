public class forloop {
    public static void main(String[] args) {
        int n=3;
        for(int i=0; i<n; i++){
            System.out.println(2*i+1);
        }
        //decrimented for loop
        for(int i=5;i>0;i--){
            System.out.println(i);
        }
        //reverseorder print
        for(int i=10;i>0;i--){
            System.out.println(i);
        }
    }
}
