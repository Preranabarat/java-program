public class sum_even_number {
    public static void main(String[] args) {
        int sum=0;
        int n=4;
        for(int i=0;i<n;i++){
            sum=sum+(2*i);
        }
        System.out.print("sum of even number is");
        System.out.println(sum);
        //while loop
        int sum1=0;
        int n1 =4;
        int i=0;
        while(i<n1){
            i++;
        }
        System.out.println("sum of even numbers");
        System.out.println(sum1);
    }
}
