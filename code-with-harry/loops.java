public class loops{
    public static void main(String[] args) {
       //while loop
        int i=0;
        while(i<=10){
            System.out.println(i);
            i++;
        }
        //do while
        int b=0;
        do{
            System.out.println(b);
            b++;
        }while(b<5);
        //forloop
        for(int i=0;i<=10;i++){
            System.out.println(i);
        }
    }
}